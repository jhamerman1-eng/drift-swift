#!/usr/bin/env python3
"""
Drift Trading Environment Setup Helper
Secure API key configuration and environment management
"""

import os
import sys
import getpass
import json
from pathlib import Path
from datetime import datetime

class EnvironmentSetup:
    def __init__(self):
        self.project_root = Path(__file__).parent
        self.env_file = self.project_root / ".env"
        self.wallet_file = self.project_root / ".devnet_wallet.json"

    def print_header(self):
        print("\nðŸ”§ Drift Trading Environment Setup Helper")
        print("=" * 50)

    def check_environment_status(self):
        """Check current environment variable status"""
        print("\nðŸ“Š Current Environment Status:")
        print("â”€" * 40)

        api_key_set = bool(os.getenv("DRIFT_API_KEY"))
        if api_key_set:
            print("âœ… DRIFT_API_KEY: Set (hidden for security)")
        else:
            print("âŒ DRIFT_API_KEY: Not set")

        cluster = os.getenv("DRIFT_CLUSTER", "")
        if cluster:
            print(f"âœ… DRIFT_CLUSTER: {cluster}")
        else:
            print("âŒ DRIFT_CLUSTER: Not set")

        if self.wallet_file.exists():
            print(f"âœ… Wallet file: Found ({self.wallet_file.name})")
        else:
            print("âŒ Wallet file: Not found")

        if self.env_file.exists():
            print("âœ… Environment file: Found (.env)")
        else:
            print("âŒ Environment file: Not found")

    def set_api_key_interactive(self):
        """Securely set API key via user input"""
        print("\nðŸ” Setting API Key (Secure Input)")
        print("â”€" * 40)
        print("ðŸ’¡ Your API key will not be displayed as you type")
        print("ðŸ’¡ Get your Helius API key from: https://helius.dev")
        print()

        try:
            api_key = getpass.getpass("Enter your Helius API Key: ").strip()

            if not api_key:
                print("âŒ No API key entered.")
                return False

            # Basic validation
            if not all(c.isalnum() or c in "-_" for c in api_key):
                print("âŒ API key contains invalid characters.")
                return False

            os.environ["DRIFT_API_KEY"] = api_key
            print(f"âœ… API key set successfully (length: {len(api_key)} characters)")
            print("ðŸ’¡ Note: This is only set for the current session")
            return True

        except KeyboardInterrupt:
            print("\nâŒ Operation cancelled.")
            return False

    def set_api_key_from_file(self):
        """Load API key from file"""
        print("\nðŸ“ Setting API Key from File")
        print("â”€" * 40)

        file_path = input("Enter path to API key file: ").strip()

        if not Path(file_path).exists():
            print(f"âŒ File not found: {file_path}")
            return False

        try:
            with open(file_path, 'r') as f:
                api_key = f.read().strip()

            if not api_key:
                print("âŒ API key file is empty")
                return False

            os.environ["DRIFT_API_KEY"] = api_key
            print("âœ… API key loaded from file successfully")
            return True

        except Exception as e:
            print(f"âŒ Error reading file: {e}")
            return False

    def generate_env_file(self):
        """Generate .env file with current configuration"""
        print("\nðŸ“„ Generating Environment File")
        print("â”€" * 40)

        api_key = os.getenv("DRIFT_API_KEY")
        if not api_key:
            print("âŒ Please set your API key first")
            return False

        env_config = {
            "DRIFT_API_KEY": api_key,
            "DRIFT_CLUSTER": "devnet",
            "DRIFT_KEYPAIR_PATH": ".devnet_wallet.json",
            "USE_MOCK": "false",
            "SWIFT_BASE": "https://swift.drift.trade"
        }

        try:
            with open(self.env_file, 'w') as f:
                f.write("# Drift Trading Environment Configuration\n")
                f.write(f"# Generated by setup_environment.py on {datetime.now()}\n")
                f.write("\n")
                f.write("# Helius API Key (get from https://helius.dev)\n")
                f.write(f"DRIFT_API_KEY={api_key}\n")
                f.write("\n")
                f.write("# Drift Configuration\n")
                f.write(f"DRIFT_CLUSTER={env_config['DRIFT_CLUSTER']}\n")
                f.write(f"DRIFT_KEYPAIR_PATH={env_config['DRIFT_KEYPAIR_PATH']}\n")
                f.write(f"USE_MOCK={env_config['USE_MOCK']}\n")
                f.write("\n")
                f.write("# Swift Configuration\n")
                f.write(f"SWIFT_BASE={env_config['SWIFT_BASE']}\n")

            print("âœ… Environment file (.env) created successfully!")
            print("\nðŸ’¡ To use this file, you can:")
            print("   â€¢ Load it manually: python setup_environment.py --load-env")
            print("   â€¢ Or integrate it into your startup scripts")

            return True

        except Exception as e:
            print(f"âŒ Error creating environment file: {e}")
            return False

    def load_env_file(self):
        """Load environment variables from .env file"""
        if not self.env_file.exists():
            print("âŒ Environment file (.env) not found")
            return False

        try:
            with open(self.env_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if line and not line.startswith('#'):
                        if '=' in line:
                            key, value = line.split('=', 1)
                            os.environ[key.strip()] = value.strip()

            print("âœ… Environment variables loaded from .env file")
            return True

        except Exception as e:
            print(f"âŒ Error loading environment file: {e}")
            return False

    def show_configuration(self):
        """Display current configuration"""
        print("\nðŸ“Š Current Configuration")
        print("â”€" * 40)

        print("ðŸ”‘ API Key Status:")
        api_key = os.getenv("DRIFT_API_KEY")
        if api_key:
            print(f"   âœ… Set (length: {len(api_key)} characters)")
        else:
            print("   âŒ Not set")

        print("\nðŸŒ Environment Variables:")
        env_vars = ["DRIFT_CLUSTER", "DRIFT_KEYPAIR_PATH", "USE_MOCK", "SWIFT_BASE"]
        for var in env_vars:
            value = os.getenv(var, "")
            print(f"   {var}: {value}")

        print("\nðŸ“ File Status:")
        files = [
            (self.wallet_file, "Wallet file"),
            (self.env_file, "Environment file"),
            (self.project_root / "requirements.txt", "Requirements file")
        ]

        for file_path, description in files:
            if file_path.exists():
                size = file_path.stat().st_size
                print(f"   âœ… {description}: Found ({file_path.name}, {size} bytes)")
            else:
                print(f"   âŒ {description}: Not found")

    def test_setup(self):
        """Run comprehensive environment tests"""
        print("\nðŸ§ª Testing Environment Setup")
        print("â”€" * 40)

        tests_passed = 0
        total_tests = 0

        # Test 1: API Key
        total_tests += 1
        if os.getenv("DRIFT_API_KEY"):
            print("âœ… Test 1 PASSED: DRIFT_API_KEY is set")
            tests_passed += 1
        else:
            print("âŒ Test 1 FAILED: DRIFT_API_KEY not set")

        # Test 2: Python version
        total_tests += 1
        if sys.version_info >= (3, 8):
            print(f"âœ… Test 2 PASSED: Python {sys.version.split()[0]} is available")
            tests_passed += 1
        else:
            print(f"âŒ Test 2 FAILED: Python 3.8+ required, found {sys.version.split()[0]}")

        # Test 3: Required packages
        total_tests += 1
        try:
            import driftpy
            print("âœ… Test 3 PASSED: driftpy is available")
            tests_passed += 1
        except ImportError:
            print("âŒ Test 3 FAILED: driftpy not installed")

        # Test 4: Wallet file
        total_tests += 1
        if self.wallet_file.exists():
            print("âœ… Test 4 PASSED: Wallet file exists")
            tests_passed += 1
        else:
            print("âŒ Test 4 FAILED: Wallet file not found")

        # Test 5: Project structure
        total_tests += 1
        required_files = ["setup_devnet_trading.bat", "requirements.txt"]
        missing_files = [f for f in required_files if not (self.project_root / f).exists()]

        if not missing_files:
            print("âœ… Test 5 PASSED: Project structure is complete")
